#include "libft.h"

char	*ft_strlowercase(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		while (str[a] >= 'A' && str[a] <= 'Z')
			str[a] = str[a] + 32;
		a++;
	}
	return (str);
}
