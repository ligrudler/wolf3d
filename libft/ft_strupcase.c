#include "libft.h"

char	*ft_strupcase(char *str)
{
	int a;

	a = 0;
	while (str[a])
	{
		while (str[a] <= 'z' && str[a] >= 'a')
			str[a] = str[a] - 32;
		a++;
	}
	return (str);
}
