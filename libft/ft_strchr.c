#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*d;

	d = (char *)s;
	i = 0;
	while (d[i] != c)
	{
		if (d[i] == '\0')
			return (NULL);
		i++;
	}
	return (d + i);
}
